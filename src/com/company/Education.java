package com.company;

import java.util.ArrayList;

public class Education {
    ArrayList<String> education = new ArrayList<>();
    ArrayList<String> univeristy  = new ArrayList<>();
    ArrayList<String> year = new ArrayList<>();

    public ArrayList<String> getEducation() {
        return education;
    }

    public void setEducation(ArrayList<String> education) {
        this.education = education;
    }

    public ArrayList<String> getUniveristy() {
        return univeristy;
    }

    public void setUniveristy(ArrayList<String> univeristy) {
        this.univeristy = univeristy;
    }

    public ArrayList<String> getYear() {
        return year;
    }

    public void setYear(ArrayList<String> year) {
        this.year = year;
    }
}
